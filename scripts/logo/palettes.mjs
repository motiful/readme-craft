export function ensurePalette(paletteKey) {
  // Support custom hex colors: "ff9966,ff5e62" or "#ff9966,#ff5e62"
  if (paletteKey && paletteKey.includes(',')) {
    return paletteKey.split(',').map(c => {
      c = c.trim();
      return c.startsWith('#') ? c : '#' + c;
    });
  }
  const entry = PALETTES[paletteKey];
  if (!entry) {
    throw new Error(`Unknown palette: ${paletteKey}. Available: ${Object.keys(PALETTES).join(', ')}. Or use custom: --palette "#hex1,#hex2"`);
  }
  // Backward compat: handle both array and object entries
  return Array.isArray(entry) ? entry : entry.colors;
}

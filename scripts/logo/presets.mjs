export function resolvePresetChoice(presetKey, headerStyle) {
  if (presetKey) return presetKey;
  const styleKey = headerStyle ?? 'single-line';
  const resolved = HEADER_STYLE_PRESETS[styleKey];
  if (!resolved) {
    throw new Error(`Unknown header style: ${styleKey}. Available: ${Object.keys(HEADER_STYLE_PRESETS).join(', ')}`);
  }
  return resolved;
}
